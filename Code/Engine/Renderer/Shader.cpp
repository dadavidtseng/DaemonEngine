#include "Engine/Renderer/Shader.hpp"

#include <d3d11.h>

//----------------------------------------------------------------------------------------------------
Shader::Shader(ShaderConfig const& config)
    : m_config(config)
{
}

//----------------------------------------------------------------------------------------------------
Shader::~Shader()
{
    m_vertexShader->Release();
    m_pixelShader->Release();
    m_inputLayout->Release();
}

//----------------------------------------------------------------------------------------------------
String const& Shader::GetName() const
{
    return m_config.m_name;
}
