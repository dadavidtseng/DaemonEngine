//----------------------------------------------------------------------------------------------------
#include "Engine/Widget/IWidget.hpp"

//----------------------------------------------------------------------------------------------------
IWidget::IWidget() = default;

//----------------------------------------------------------------------------------------------------
IWidget::~IWidget() = default;

//----------------------------------------------------------------------------------------------------
void IWidget::BeginFrame()
{
}

//----------------------------------------------------------------------------------------------------
void IWidget::Render()
{
    if (m_bIsVisible && !m_bIsGarbage)
    {
        Draw();
    }
}

//----------------------------------------------------------------------------------------------------
void IWidget::Draw() const
{
}

//----------------------------------------------------------------------------------------------------
void IWidget::Update()
{
}

//----------------------------------------------------------------------------------------------------
void IWidget::EndFrame()
{
}

//----------------------------------------------------------------------------------------------------
uint64_t IWidget::GetOwner() const
{
    return m_ownerID;
}

//----------------------------------------------------------------------------------------------------
int IWidget::GetZOrder() const
{
    return m_zOrder;
}

//----------------------------------------------------------------------------------------------------
String IWidget::GetName() const
{
    return m_name;
}

//----------------------------------------------------------------------------------------------------
bool IWidget::IsVisible() const
{
    return m_bIsVisible;
}

//----------------------------------------------------------------------------------------------------
bool IWidget::IsTick() const
{
    return m_bIsTick;
}

//----------------------------------------------------------------------------------------------------
bool IWidget::IsGarbage() const
{
    return m_bIsGarbage;
}

//----------------------------------------------------------------------------------------------------
bool IWidget::IsModal() const
{
    return false;  // Default: widgets do not block input to lower layers
}

//----------------------------------------------------------------------------------------------------
void IWidget::SetOwner(uint64_t ownerID)
{
    m_ownerID = ownerID;
}

//----------------------------------------------------------------------------------------------------
void IWidget::SetZOrder(int zOrder)
{
    m_zOrder = zOrder;
}

//----------------------------------------------------------------------------------------------------
void IWidget::SetName(String const& name)
{
    m_name = name;
}

//----------------------------------------------------------------------------------------------------
void IWidget::SetVisible(bool visible)
{
    m_bIsVisible = visible;
}

//----------------------------------------------------------------------------------------------------
void IWidget::SetTick(bool tick)
{
    m_bIsTick = tick;
}

//----------------------------------------------------------------------------------------------------
void IWidget::MarkForDestroy()
{
    m_bIsGarbage = true;
}
